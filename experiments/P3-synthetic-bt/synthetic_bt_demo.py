"""P3: Synthetic Bradley-Terry with known reward.

Theory anchor (Year-1 target's empirical validation).

Setup
-----
- Reward r : Y → R drawn from N(0, 1).
- Preference generated by Bradley-Terry:
      P(y_A ≻ y_B) = sigmoid(r(y_A) - r(y_B))
- True Preference Bayes Error (analytic, from BT):
      R*_pref = 1/2 - (1/2) E_Δ[|tanh(Δ/2)|]
  where Δ ~ r(y_A) - r(y_B).

Goal
----
Verify empirically that:
  (i) Plug-in R̂* (given soft labels) is UNBIASED estimator of R*_pref.
  (ii) Concentration |R̂* - R*| = O_P(N^{-1/2}) (slope = -0.5 in log-log).
  (iii) Under BT noise, the margin formula R*_pref = 1/2 - E[|tanh(Δ/2)|]/2
        gives the same number as the instance-wise min(η, 1-η) formula.

Outputs
-------
- fig_synthetic_bt.png        (4-panel: R*_analytic vs empirical, N-scaling)
- results_synthetic_bt.json   (numerical results)
"""
import json
import math
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np
from scipy.stats import bootstrap

HERE = Path(__file__).resolve().parent
rng = np.random.default_rng(42)

# ------------- Reward draw -------------
N_Y = 100_000          # large pool of responses
r_true = rng.standard_normal(N_Y)

# ------------- Analytic R*_pref via margin formula -------------
# Sample pairs (i, j), compute Δ = r_i - r_j, then E[|tanh(Δ/2)|]
N_ANALYTIC = 500_000
idx_A = rng.integers(0, N_Y, size=N_ANALYTIC)
idx_B = rng.integers(0, N_Y, size=N_ANALYTIC)
delta = r_true[idx_A] - r_true[idx_B]
R_star_analytic = 0.5 - 0.5 * float(np.mean(np.abs(np.tanh(delta / 2))))
print(f"True R*_pref (analytic via E[|tanh(Δ/2)|]/2 formula): {R_star_analytic:.5f}")

# Cross-check via instance-wise formula: E[min(η, 1-η)]
eta = 1.0 / (1.0 + np.exp(-delta))
R_star_minform = float(np.mean(np.minimum(eta, 1 - eta)))
print(f"True R*_pref (via min(η, 1-η) formula):             {R_star_minform:.5f}")
assert abs(R_star_analytic - R_star_minform) < 1e-3, "Two formulas must agree"

# ------------- Empirical plug-in estimator at various N -------------
N_values = [100, 200, 500, 1_000, 2_000, 5_000, 10_000, 20_000, 50_000]
N_REPEATS = 50  # seeds per N for variance

all_results = []
for N in N_values:
    rstar_estimates = []
    for seed in range(N_REPEATS):
        r = np.random.default_rng(1000 * seed + 13)
        ia = r.integers(0, N_Y, size=N)
        ib = r.integers(0, N_Y, size=N)
        d = r_true[ia] - r_true[ib]
        # Draw preferences via BT; we receive "soft labels" c_i = sigmoid(d)
        # (perfect soft labels — the Ishida 2023 setting).
        eta_i = 1.0 / (1.0 + np.exp(-d))
        R_hat = float(np.mean(np.minimum(eta_i, 1 - eta_i)))
        rstar_estimates.append(R_hat)
    rstar_estimates = np.array(rstar_estimates)
    err = np.abs(rstar_estimates - R_star_analytic)
    all_results.append({
        "N": N,
        "R_hat_mean": float(rstar_estimates.mean()),
        "R_hat_std": float(rstar_estimates.std()),
        "abs_err_mean": float(err.mean()),
        "abs_err_std": float(err.std()),
        "abs_err_p95": float(np.percentile(err, 95)),
    })
    print(f"N={N:>6}: R̂* = {rstar_estimates.mean():.5f} ± {rstar_estimates.std():.5f}  "
          f"|R̂*-R*| mean = {err.mean():.5f}")

# ------------- Plot: 2x2 figure -------------
fig, axes = plt.subplots(2, 2, figsize=(12, 9))

# Panel A: analytic vs empirical R*_pref at largest N
ax = axes[0, 0]
Ns = [r["N"] for r in all_results]
Rhats_mean = np.array([r["R_hat_mean"] for r in all_results])
Rhats_std = np.array([r["R_hat_std"] for r in all_results])
ax.errorbar(Ns, Rhats_mean, yerr=Rhats_std, fmt="o-", capsize=4,
            color="#2b6cb0", label="Plug-in R̂* (mean ± std over 50 seeds)")
ax.axhline(R_star_analytic, color="red", ls="--", lw=2,
           label=f"Analytic R*_pref = {R_star_analytic:.4f}")
ax.set_xscale("log")
ax.set_xlabel("N (number of preference pairs)")
ax.set_ylabel(r"$\hat{R}^*_{\mathrm{pref}}$")
ax.set_title("A. Plug-in estimator converges to analytic R*_pref")
ax.legend()
ax.grid(True, alpha=0.3)

# Panel B: |R̂* - R*| log-log N-scaling (O(N^-1/2) verification)
ax = axes[0, 1]
errs = np.array([r["abs_err_mean"] for r in all_results])
errs_std = np.array([r["abs_err_std"] for r in all_results])
# linear fit in log-log
log_N = np.log(np.array(Ns, dtype=float))
log_err = np.log(errs)
slope, intercept = np.polyfit(log_N, log_err, 1)
ax.errorbar(Ns, errs, yerr=errs_std, fmt="o", capsize=4,
            color="#c05621", label="Empirical mean |R̂* - R*|")
fit_line = np.exp(intercept) * np.array(Ns, dtype=float) ** slope
ax.plot(Ns, fit_line, "--", color="black", lw=1.5,
        label=f"Fit: slope = {slope:.3f} (theory: -0.500)")
theory_const = errs[0] * Ns[0] ** 0.5
ax.plot(Ns, theory_const * np.array(Ns, dtype=float) ** -0.5, ":", color="gray",
        label="Theoretical O(N^{-1/2})")
ax.set_xscale("log")
ax.set_yscale("log")
ax.set_xlabel("N")
ax.set_ylabel("|R̂* - R*|")
ax.set_title(f"B. N-scaling: slope = {slope:+.3f} (expected -0.500)")
ax.legend()
ax.grid(True, which="both", alpha=0.3)

# Panel C: Δ distribution (reward margin) + |tanh(Δ/2)|
ax = axes[1, 0]
delta_sub = delta[:20_000]
ax.hist(delta_sub, bins=60, density=True, alpha=0.6, color="#38a169",
        label=r"$\Delta = r(y_A) - r(y_B)$")
ax.set_xlabel(r"Reward margin $\Delta$")
ax.set_ylabel("density")
ax.set_title(r"C. Reward margin distribution (from $r \sim \mathcal{N}(0,1)$)")
ax.grid(True, alpha=0.3)
ax.legend()

# Panel D: η = sigmoid(Δ) distribution + min(η, 1-η) region shaded
ax = axes[1, 1]
eta_sub = 1.0 / (1.0 + np.exp(-delta_sub))
ax.hist(eta_sub, bins=50, density=True, alpha=0.6, color="#6b46c1",
        label=r"$\eta = \sigma(\Delta)$")
ax.axvline(0.5, color="red", ls="--", label=r"$\eta = 1/2$ (ambiguous)")
ax.set_xlabel(r"$\eta = P(y_A \succ y_B \mid \Delta)$")
ax.set_ylabel("density")
ax.set_title(rf"D. Posterior dist. $\to\ R^*_{{pref}} = {R_star_analytic:.4f}$")
ax.legend()
ax.grid(True, alpha=0.3)

fig.suptitle(
    f"P3 · Synthetic BT with Known Reward (r ~ N(0,1), 50 seeds/N, analytic R*_pref = {R_star_analytic:.4f})",
    fontsize=11, y=1.02,
)
fig.tight_layout()
fig.savefig(HERE / "fig_synthetic_bt.png", dpi=150, bbox_inches="tight")

# ------------- Save results -------------
out = {
    "r_distribution": "Normal(0, 1)",
    "N_Y_pool": N_Y,
    "N_analytic_samples": N_ANALYTIC,
    "R_star_pref_analytic_tanh_formula": R_star_analytic,
    "R_star_pref_analytic_min_formula": R_star_minform,
    "N_scaling_results": all_results,
    "log_log_slope": float(slope),
    "log_log_intercept": float(intercept),
    "theoretical_slope": -0.5,
    "slope_deviation": float(abs(slope - (-0.5))),
    "n_repeats_per_N": N_REPEATS,
    "seed": 42,
}
with open(HERE / "results_synthetic_bt.json", "w") as f:
    json.dump(out, f, indent=2)

print(f"\nSlope of log-log fit: {slope:.3f} (expected -0.500, deviation {abs(slope-(-0.5)):.3f})")
print(f"\nSaved: {HERE / 'fig_synthetic_bt.png'}")
print(f"Saved: {HERE / 'results_synthetic_bt.json'}")
